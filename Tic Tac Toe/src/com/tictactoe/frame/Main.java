    private void clearAll(){
        card.show(cardPanel, "option");
        lblMode.setText("");
        lblTurn.setText("");
        lblO.setText("");
        lblX.setText("");
        MyButtonGroup.setText("", btnGroupGame);
    }
    
    private void playerMoviment(int x, int y){
        if(t3.isEmpty(x, y)){
            if(lblX.getText().equals(lblTurn.getText())){
                t3.setCoordenate(x, y, TicTacToe.CHAR_X);
            }else{
                t3.setCoordenate(x, y, TicTacToe.CHAR_O);
            }
            t3.incrementCount();
            if(lblTurn.getText().equals(lblX.getText())){
                if(t3.win(TicTacToe.CHAR_X)){
                    JOptionPane.showMessageDialog(this, lblTurn.getText()+" win!", "WIN", 1);
                    clearAll();
                }
            }else{
                if(t3.win(TicTacToe.CHAR_O)){
                    JOptionPane.showMessageDialog(this, lblTurn.getText()+" win!", "WIN", 1);
                    clearAll();
                }
            }
            if(t3.getCount()==9){
                JOptionPane.showMessageDialog(this, "Tie", "TIE", 1);
                clearAll();
            }
            if(lblTurn.getText().equals(lblX.getText())){
                lblTurn.setText(lblO.getText());
            }else{
                lblTurn.setText(lblX.getText());
            }
        }
    }
    
